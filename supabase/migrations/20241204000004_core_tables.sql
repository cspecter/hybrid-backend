-- ============================================================================
-- UNIFIED TIMESTAMP MANAGEMENT
-- ============================================================================

CREATE OR REPLACE FUNCTION public.manage_timestamps()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
    IF TG_OP = 'INSERT' THEN
        NEW.created_at := COALESCE(NEW.created_at, now());
        NEW.updated_at := now();
        RETURN NEW;
    END IF;
    
    IF TG_OP = 'UPDATE' THEN
        NEW.created_at := OLD.created_at;
        NEW.updated_at := now();
        RETURN NEW;
    END IF;
    
    RETURN NEW;
END;
$$;

-- Helper to create the trigger on any table
CREATE OR REPLACE FUNCTION public.create_timestamps_trigger(table_name text)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
    EXECUTE format(
        'CREATE TRIGGER trg_timestamps 
         BEFORE INSERT OR UPDATE ON public.%I 
         FOR EACH ROW EXECUTE FUNCTION public.manage_timestamps()',
        table_name
    );
END;
$$;

