-- Helper function to search locations by features
CREATE OR REPLACE FUNCTION public.search_locations_by_features(
    feature_filters jsonb,
    near_lat double precision DEFAULT NULL,
    near_lng double precision DEFAULT NULL,
    radius_miles double precision DEFAULT 50
)
RETURNS SETOF public.locations
LANGUAGE sql
STABLE
AS $$
    SELECT l.*
    FROM public.locations l
    WHERE 
        l.status = 'published'
        AND l.features @> feature_filters
        AND (
            near_lat IS NULL 
            OR extensions.ST_DWithin(
                l.coordinates,
                extensions.ST_SetSRID(extensions.ST_MakePoint(near_lng, near_lat), 4326)::extensions.geography,
                radius_miles * 1609.34
            )
        )
    ORDER BY 
        CASE 
            WHEN near_lat IS NOT NULL 
            THEN extensions.ST_Distance(
                l.coordinates,
                extensions.ST_SetSRID(extensions.ST_MakePoint(near_lng, near_lat), 4326)::extensions.geography
            )
            ELSE 0 
        END;
$$;

